package scanner;

import java.util.Scanner;

public class SwichCase {

	public static void main(String[] args) {
		System.out.println("Please enter your choice number R/B/G");
		Scanner sc=new Scanner(System.in);
		
		int choice=sc.nextInt();
		
	switch(choice) {
	
	case 1:
		System.out.println("it is Red");
		System.out.println("Red is to stop");
		break;
		
	case 2:
		System.out.println("it is Black");
		System.out.println("Black is to stop");
		break;
		
	case 3:
		System.out.println("it is Green");
		System.out.println("green is to go");
		
		
		
		
		
	
	
	
	
	
	}
	
	
	
	}

}
